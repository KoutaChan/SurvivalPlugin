package xyz.n7mn.dev.survivalplugin.function;

public class Moji {

    private final String key;
    private final String value;

    public Moji(String key, String value){
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }

}
